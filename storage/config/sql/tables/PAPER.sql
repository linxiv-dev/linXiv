CREATE TABLE PAPER(
    PAPER_SK INTEGER,
    DOI VARCHAR(255),
    `VERSION` INTEGER NOT NULL,
    TITLE varchar(255),
    TOPIC_PRIMARY_SK INTEGER,
    CONTENT_SK INTEGER,
    PRIMARY_AUTHOR_SK INTEGER,
    PRIMARY KEY PAPER_SK,
    FOREIGN KEY (CONTENT_SK) REFERENCES CONTENT(CONETNET_SK),
    FOREIGN KEY (PRIMARY_AUTHOR_SK) REFERENCES AUTHOR(AUTHOR_SK),
    FOREIGN KEY (PAPER_SK) REFERENCES PAPER(PAPER_SK)

);